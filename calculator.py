# calculator.py - Simple calculator for Git workshop

def add(a, b):
    """Return the sum of a and b."""
    return a + b

def subtract(a, b):
    """Return the difference of a and b."""
    return a - b

if __name__ == "__main__":
    print("Addition:    ", add(10, 5))
    print("Subtraction: ", subtract(10, 5))
